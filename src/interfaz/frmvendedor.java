     //CREAR EL MÃ‰TODO PARA INGRESAR DATOS A LA BASE DATOS
    public static void InsertarDatos(Connection con){
        try{
            //CREAR UNA VARIABLE PARA ALMACENAR LA INSTRUCCION SQL PARA INSERTAR
            String SQL="INSERT INTO empleado VALUES(" + Integer.parseInt(txtcodigo.getText()) + 
                    ",'" + txtnombre.getText() + "','" + txtapellido.getText()+ "')";
            //PARA PODER LOS DATOS EN LA BASE DATOS, SE DEBE CREAR UNA VARIABLE STATAMENT
            Statement stmt=con.createStatement();
            stmt.executeUpdate(SQL);
            JOptionPane.showMessageDialog(null,"Se han ingresado el vendedor");
            stmt.close();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }
